#include <stdio.h>

int main(void)
{
	// 字符数据类型 => 1
	printf("%zu\n", sizeof(char));

	// 短整型 => 2
	printf("%zu\n", sizeof(short));

	// 整型 => 4
	printf("%zu\n", sizeof(int));

	// 长整型 => 4
	printf("%zu\n", sizeof(long));

	// 长长整型 => 8
	printf("%zu\n", sizeof(long long));

	// 单精度浮点 => 4
	printf("%zu\n", sizeof(float));

	// 双精度浮点 => 8
	printf("%zu\n", sizeof(double));
	

	return 0;
}